async function run() {
    const lines = data.split('\n');
    let othersPayload = [];
    let compressorPayload = [];

    for (let i = 1; i < lines.length; i++) {
        let cols = lines[i].split('\t');
        if (cols.length < 2) continue; // Skip empty
        
        // Parse date (M/D/YYYY -> YYYY-MM-DD)
        const dateStr = cols[0].trim();
        if (!dateStr) continue;
        const [m, d, y] = dateStr.split('/');
        const isoDate = `${y}-${m.padStart(2, '0')}-${d.padStart(2, '0')}`;

        // Parse values
        const parseNum = (val, divide) => {
            if (!val) return undefined;
            const clean = val.replace(/,/g, '').trim();
            if (clean === '') return undefined;
            let num = Number(clean);
            if (isNaN(num)) return undefined;
            if (divide) num = num / 100;
            return num;
        };

        const transformer = parseNum(cols[1]);
        const maintenance = parseNum(cols[2]);
        const eco2 = parseNum(cols[3]);
        const db_ac_hca = parseNum(cols[4]);
        // cols[5] is Color Sorter, skip for now.
        const mnk1 = parseNum(cols[6]);
        const office = parseNum(cols[7], true); // /100 logic

        const othersRow = {
            work_date: isoDate,
            transformer: transformer,
            maintenance: maintenance,
            eco2: eco2,
            db_ac_hca: db_ac_hca,
            office: office,
            updated_at: new Date().toISOString()
        };
        // Clean undefined props
        Object.keys(othersRow).forEach(k => othersRow[k] === undefined && delete othersRow[k]);
        
        if (Object.keys(othersRow).length > 2) {
            othersPayload.push(othersRow);
        }

        if (mnk1 !== undefined) {
            compressorPayload.push({
                work_date: isoDate,
                meter1: mnk1,
                updated_at: new Date().toISOString()
            });
        }
    }

    console.log(`Prepared ${othersPayload.length} rows for Others and ${compressorPayload.length} for Compressor.`);

    // Upsert others
    if (othersPayload.length > 0) {
        const { error: othersError } = await supabase
            .from('daily_electricity_others')
            .upsert(othersPayload, { onConflict: 'work_date' });
        
        if (othersError) console.error("Error upserting Others:", othersError);
        else console.log("Successfully upserted Others.");
    }

    // Upsert compressor
    if (compressorPayload.length > 0) {
        // Need to fetch current compressor data to not overwrite meter2, meter3 if they exist
        // Since we are just updating meter1, an upsert might overwrite meter2 with null if we don't supply it.
        // Actually, Supabase upsert will only overwrite specified columns if we use update logic, but standard upsert inserts defaults.
        // Let's do a safe upsert by merging.
        
        const { data: existingComp } = await supabase.from('daily_compressor').select('*');
        const compMap = new Map((existingComp || []).map(r => [r.work_date, r]));

        const safeCompressorPayload = compressorPayload.map(newRec => {
            const existing = compMap.get(newRec.work_date) || {};
            return {
                ...existing,
                work_date: newRec.work_date,
                meter1: newRec.meter1,
                updated_at: newRec.updated_at
            };
        });

        const { error: compError } = await supabase
            .from('daily_compressor')
            .upsert(safeCompressorPayload, { onConflict: 'work_date' });

        if (compError) console.error("Error upserting Compressor:", compError);
        else console.log("Successfully upserted Compressor.");
    }
}

run();
